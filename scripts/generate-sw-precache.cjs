'use strict';
/**
 * Generate sw-precache-manifest.js for the service worker install cache.
 * Run before production build: npm run generate-sw-precache
 */

const fs = require('fs');
const path = require('path');

const PUBLIC = path.resolve(__dirname, '..', 'game', 'public');
const OUT = path.join(PUBLIC, 'sw-precache-manifest.js');

const CORE = [
    '/',
    '/index.html',
    '/css/styles.css',
    '/css/balatro-effects.css',
    '/css/juice-effects.css',
    '/css/visual-tokens.css',
    '/manifest.json',
];

function walkFiles(dir, baseUrl = '') {
    const out = [];
    if (!fs.existsSync(dir)) return out;
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const full = path.join(dir, entry.name);
        const url = `${baseUrl}/${entry.name}`.replace(/\\/g, '/');
        if (entry.isDirectory()) {
            if (entry.name === 'Music') continue;
            out.push(...walkFiles(full, url));
        } else if (entry.isFile()) {
            const lower = entry.name.toLowerCase();
            if (lower.endsWith('.png') || lower.endsWith('.ogg') || lower.endsWith('.ttf')) {
                out.push(url);
            }
        }
    }
    return out;
}

function main() {
    const art = walkFiles(path.join(PUBLIC, 'ART'), '/ART');
    const sounds = walkFiles(path.join(PUBLIC, 'sounds'), '/sounds');
    const assets = [...new Set([...CORE, ...art, ...sounds])].sort();

    const body = `/**
 * AUTO-GENERATED by scripts/generate-sw-precache.cjs — do not edit by hand.
 * Regenerate: npm run generate-sw-precache
 */
self.__SW_PRECACHE_ASSETS = ${JSON.stringify(assets, null, 2)};
`;

    fs.writeFileSync(OUT, body);
    console.log(`[generate-sw-precache] Wrote ${assets.length} URLs → ${path.relative(process.cwd(), OUT)}`);
}

main();
